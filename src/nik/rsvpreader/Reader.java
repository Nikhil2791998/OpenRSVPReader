    private void mnupasteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnupasteActionPerformed
        // TODO add your handling code here:
        Toolkit toolkit = Toolkit.getDefaultToolkit();
        Clipboard clipboard = toolkit.getSystemClipboard();
        try{String result = (String) clipboard.getData(DataFlavor.stringFlavor);initme(result);}catch(UnsupportedFlavorException | IOException e){e.printStackTrace();}
    }//GEN-LAST:event_mnupasteActionPerformed

    private void mnuopenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnuopenActionPerformed
        // TODO add your handling code here:
        if(openFileDialog.showOpenDialog(jPanel1)==JFileChooser.APPROVE_OPTION){
            try{
                Scanner sc = new Scanner(openFileDialog.getSelectedFile());
                String str ="";
                while(sc.hasNext())str+=" "+sc.nextLine();
                initme(str);
            }catch(FileNotFoundException e){e.printStackTrace();}
        }
    }//GEN-LAST:event_mnuopenActionPerformed

