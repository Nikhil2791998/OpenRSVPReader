    private void initme(String s){
        ReaderCore.stop();
        if(s==null || s.isEmpty()){isText=false;}
        else{
            isText=true;
            ReaderCore.setString(s);
            lblmain.setText(ReaderCore.getWordat(0));
            slider.setMaximum(s.split(" ").length);
        }
        btnplay.setEnabled(isText);
    }
   
