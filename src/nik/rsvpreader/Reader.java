    private void btnplayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnplayActionPerformed
        if(null==btnplay.getText())throw new IllegalStateException("Only Two States are possible");
        else // TODO add your handling code here:
        switch (btnplay.getText()) {
            case "Pause":
                ReaderCore.pause();
                btnplay.setText("Play");
                break;
            case "Play":
                ReaderCore.start();
                btnplay.setText("Pause");
                break;
            default:
                throw new IllegalStateException("Only Two States are possible");
        }
    }//GEN-LAST:event_btnplayActionPerformed

